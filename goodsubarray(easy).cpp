void solve()
{
    int n;
    cin>>n;
    vl arr(n+1);
    ll sum=0;
    for(int i=1;i<=n;i++)
    {
        ll x;
        cin>>x;
        arr[i]=min(arr[i-1]+1,x);
        sum+=arr[i];
    }
    cout<<sum<<'\n';
}


