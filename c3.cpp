void solve()
{
    ll x,y,k;
    cin>>x>>y>>k;
    ll turn=0;
    ll c,g;
    turn=min(x,y)/k*2;
    if(min(x,y)%k)
    turn+=2;
    if(x<y)
    {
        c=turn/2*k;
        g=y;
    }
    if(y<x)
    {
        turn--;
        c=turn/2*k;
        g=x;
    }
    if(g<c)
    {
        turn+=g/c*2;
    }
    cout<<turn<<'\n';
}

