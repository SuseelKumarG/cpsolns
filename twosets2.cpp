long long gcdl(long long a, long long b){
    while(a > 0 && b > 0){
        if(a > b){
            a %= b;
        }
        else{
            b %= a;
        }
    }
    return a + b;
}  
ll lcml(ll a,ll b)
{
    a=(a*b)/gcdl(a,b);
    return a;
}
int gcd(int a, int b){
    while(a > 0 && b > 0){
        if(a > b){
            a %= b;
        }
        else{
            b %= a;
        }
    }
    return a + b;
}
int lcml(int a,int b)
{
    a=(a*b)/gcd(a,b);
    return a;
}

ll binexp(ll a,ll b)
{
    ll ans=1;
    while (b)
    {
        if(b&1)
        ans=(ans*a)%MAX;
        b>>=1;
        a=(a*a)%MAX;
    }
    return ans;
}

void solve()
{
    int n;
    cin>>n;
    ll sum=(n*(n+1))/2;
    if(sum&1)
    {
        cout<<0<<'\n';
        return;
    }
    vvi dp(n+1,vi(sum));
    dp[0][0]=1;
    for(int i=1;i<=n;i++)
    {
        for(int j=0;j<=sum/2;j++)
        {
            dp[i][j]+=dp[i-1][j];
            if(j>=i)
            dp[i][j]+=dp[i-1][j-i];
            dp[i][j]%=MAX;
        }
    }
    ll inv=binexp(2,MAX-2);
    cout<<(dp[n][sum/2]*1LL*inv)%MAX<<'\n';
}

