long long gcdl(long long a, long long b){
    while(a > 0 && b > 0){
        if(a > b){
            a %= b;
        }
        else{
            b %= a;
        }
    }
    return a + b;
}  
ll lcml(ll a,ll b)
{
    a=(a*b)/gcdl(a,b);
    return a;
}
int gcd(int a, int b){
    while(a > 0 && b > 0){
        if(a > b){
            a %= b;
        }
        else{
            b %= a;
        }
    }
    return a + b;
}
int lcml(int a,int b)
{
    a=(a*b)/gcd(a,b);
    return a;
}

void solve()
{
    string s;
    cin>>s;
    ll x;
    cin>>x;
    x--;
    int n=s.length();
    s+='0';
    bool finish=x<n;
    string ans;
    for(auto it:s)
    {
        while (!finish&&ans.size()>0&&ans.back()>it)
        {
            ans.pop_back();
            x-=n;
            n--;
            if(x<n)
            finish=1;
        }
        ans.push_back(it);
    }
    cout<<ans[x];
}

